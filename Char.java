class Char
{
	public static void main(String[] args)
	{
		char letter='S';
		char firstIntial='J';
		char branch='I';
		char gender='M';
		char section='A';
		
		System.out.println("Initinal Values;");
		System.out.println("Letter: "+letter);
		System.out.println("First Intial: "+firstIntial);
		System.out.println("Branch: "+branch);
		System.out.println("Gender: "+gender);
		System.out.println("Section: "+section);
		
		
		letter='P';
		firstIntial='N';
		branch='S';
		gender='F';
		section='B';
		
		System.out.println("Initinal Values;");
		System.out.println("Letter: "+letter);
		System.out.println("First Intial: "+firstIntial);
		System.out.println("Branch: "+branch);
		System.out.println("Gender: "+gender);
		System.out.println("Section: "+section);
	}
}