class Boolean
{
	public static void main(String[] args)
	{
		boolean isActive=true;
		boolean hasPermission=false;
		boolean canVote=true;
		boolean shouldUpdate=false;
		boolean _flag=true;
		
		
		System.out.println("Initinal Values");
		System.out.println("Is Active :"+isActive);
		System.out.println("Has Permission : "+hasPermission);
		System.out.println("Can Vote: "+canVote);
		System.out.println("Should Update: "+shouldUpdate);
		System.out.println("Flag: "+_flag);
		
		isActive=false;
		hasPermission=true;
		canVote=false;
		shouldUpdate=true;
		_flag=false;
		
		
		System.out.println("Re-Initinal Values;");
		System.out.println("Is Active :"+isActive);
		System.out.println("Has Permission : "+hasPermission);
		System.out.println("Can Vote: "+canVote);
		System.out.println("Should Update: "+shouldUpdate);
		System.out.println("Flag: "+_flag);
		
		
		
		
	}
}