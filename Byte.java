class Byte
{
	public static void main(String[] args)
	{
		byte call=10;
		byte rohan=21;
		byte family=30;
		byte keys=50;
		byte dream=15;
		System.out.println("Initinal Values");
		System.out.println("No of calls: "+call);
		System.out.println("rohan's age: "+rohan);
		System.out.println("family: "+family);
		System.out.println("keys: "+keys);
		System.out.println("dream: "+dream);
		
		call=5;
		rohan=15;
		family=25;
		keys=45;
		dream=4;
		System.out.println("Re-Initinal values: ");
		System.out.println("No of calls: "+call);
		System.out.println("rohan's age: "+rohan);
		System.out.println("family: "+family);
		System.out.println("Keys: "+keys);
		System.out.println("dream: "+dream);
	}
}